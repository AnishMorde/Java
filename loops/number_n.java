
import java.util.*;
public class number_n {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        System.out.print("Enter the range=");
        int n = sc.nextInt();
        int counter=1;
        while(counter<=n){
            System.out.println(counter);
            counter++; 
        }




        

    }
}

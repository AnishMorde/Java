public class switch1 {
    public static void main(String[] args) {
        int number= 10 ;

        switch(number){
            case 1: System.out.println("Mango");
                     break;

             case 2: System.out.println("burger");
                     break;

            case 3: System.out.println("orange");
                     break;
            default: System.out.println("Invalid choice");
            break;         


        }
        
    }
    
}
